function buildMap(): Map<string, string> {
  const map = new Map<string, string>();
  // SC and TC are matched by character index
  for (let i = 0; i < SC.length; i++) {
    map.set(SC[i]!, TC[i]!);
  }
  return map;
}

const charMap = buildMap();

/** The total number of character mappings */
export const MAPPING_COUNT = charMap.size;

/**
 * Convert a Simplified Chinese string to Traditional Chinese.
 * Characters not in the mapping table pass through unchanged.
 */
export function convertToTraditional(text: string): string {
  let result = '';
  for (let i = 0; i < text.length; i++) {
    const char = text[i]!;
    result += charMap.get(char) ?? char;
  }
  return result;
}
